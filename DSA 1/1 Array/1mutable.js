// Immutable - Primitive Data Structures
let a = "hello";

let b= a+ "how are you";

console.log(a,b);

// Mutable - Non-Primitive Data Structures

let myArray = [1, 2, 3, 4];

myArray[2] = 10;  // Change the value at index 2
myArray.push(5);  // Add a new element at the end

console.log(myArray);  // Output: [1, 2, 10, 4, 5]